export type IntlEventMessageKey = (typeof INTERNATIONAL_RECURRING)[number]['messageKey'];
export type InternationalRecurringRule = (typeof INTERNATIONAL_RECURRING)[number];

export function getInternationalEventKeysForDate(d: Date): IntlEventMessageKey[] {
  const m = d.getMonth();
  const day = d.getDate();
  return INTERNATIONAL_RECURRING.filter(r => r.month === m && r.day === day).map(r => r.messageKey);
}
